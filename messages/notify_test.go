package messages

import (
	"testing"

	"github.com/vazir/m3ua-go/messages/params"
)

func TestNotify(t *testing.T) {
	cases := []testCase{
		{
			"has-all-params",
			NewNotify(
				params.NewStatus(params.AsStateActive),
				params.NewAspIdentifier(2),
				params.NewRoutingContext(1),
				params.NewInfoString("deadbeef"),
			),
			[]byte{
				// Header
				0x01, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x2c,
				// Status
				0x00, 0x0d, 0x00, 0x08, 0x00, 0x01, 0x00, 0x03,
				// AspIdentifier
				0x00, 0x11, 0x00, 0x08, 0x00, 0x00, 0x00, 0x02,
				// RoutingContext
				0x00, 0x06, 0x00, 0x08, 0x00, 0x00, 0x00, 0x01,
				// InfoString
				0x00, 0x04, 0x00, 0x0c, 0x64, 0x65, 0x61, 0x64,
				0x62, 0x65, 0x65, 0x66,
			},
		}, {
			"has-asp-rc",
			NewNotify(
				params.NewStatus(params.AsStateActive),
				params.NewAspIdentifier(2),
				params.NewRoutingContext(1),
				nil,
			),
			[]byte{
				// Header
				0x01, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x20,
				// Status
				0x00, 0x0d, 0x00, 0x08, 0x00, 0x01, 0x00, 0x03,
				// AspIdentifier
				0x00, 0x11, 0x00, 0x08, 0x00, 0x00, 0x00, 0x02,
				// RoutingContext
				0x00, 0x06, 0x00, 0x08, 0x00, 0x00, 0x00, 0x01,
			},
		}, {
			"has-asp-info",
			NewNotify(
				params.NewStatus(params.AsStateActive),
				params.NewAspIdentifier(2),
				nil,
				params.NewInfoString("deadbeef"),
			),
			[]byte{
				// Header
				0x01, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x24,
				// Status
				0x00, 0x0d, 0x00, 0x08, 0x00, 0x01, 0x00, 0x03,
				// AspIdentifier
				0x00, 0x11, 0x00, 0x08, 0x00, 0x00, 0x00, 0x02,
				// InfoString
				0x00, 0x04, 0x00, 0x0c, 0x64, 0x65, 0x61, 0x64,
				0x62, 0x65, 0x65, 0x66,
			},
		}, {
			"has-rc-info",
			NewNotify(
				params.NewStatus(params.AsStateActive),
				nil,
				params.NewRoutingContext(1),
				params.NewInfoString("deadbeef"),
			),
			[]byte{
				// Header
				0x01, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x24,
				// Status
				0x00, 0x0d, 0x00, 0x08, 0x00, 0x01, 0x00, 0x03,
				// RoutingContext
				0x00, 0x06, 0x00, 0x08, 0x00, 0x00, 0x00, 0x01,
				// InfoString
				0x00, 0x04, 0x00, 0x0c, 0x64, 0x65, 0x61, 0x64,
				0x62, 0x65, 0x65, 0x66,
			},
		}, {
			"has-asp",
			NewNotify(
				params.NewStatus(params.AsStateActive),
				params.NewAspIdentifier(2),
				nil,
				nil,
			),
			[]byte{
				// Header
				0x01, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x18,
				// Status
				0x00, 0x0d, 0x00, 0x08, 0x00, 0x01, 0x00, 0x03,
				// AspIdentifier
				0x00, 0x11, 0x00, 0x08, 0x00, 0x00, 0x00, 0x02,
			},
		}, {
			"has-rc",
			NewNotify(
				params.NewStatus(params.AsStateActive),
				nil,
				params.NewRoutingContext(1),
				nil,
			),
			[]byte{
				// Header
				0x01, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x18,
				// Status
				0x00, 0x0d, 0x00, 0x08, 0x00, 0x01, 0x00, 0x03,
				// RoutingContext
				0x00, 0x06, 0x00, 0x08, 0x00, 0x00, 0x00, 0x01,
			},
		}, {
			"has-info",
			NewNotify(
				params.NewStatus(params.AsStateActive),
				nil,
				nil,
				params.NewInfoString("deadbeef"),
			),
			[]byte{
				// Header
				0x01, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x1c,
				// Status
				0x00, 0x0d, 0x00, 0x08, 0x00, 0x01, 0x00, 0x03,
				// InfoString
				0x00, 0x04, 0x00, 0x0c, 0x64, 0x65, 0x61, 0x64,
				0x62, 0x65, 0x65, 0x66,
			},
		}, {
			"has-status-only",
			NewNotify(
				params.NewStatus(params.AsStateActive),
				nil, nil, nil,
			),
			[]byte{
				// Header
				0x01, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x10,
				// Status
				0x00, 0x0d, 0x00, 0x08, 0x00, 0x01, 0x00, 0x03,
			},
		},
		{
			"has-none",
			NewNotify(nil, nil, nil, nil),
			[]byte{
				// Header
				0x01, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x08,
			},
		},
	}

	runTests(t, cases, func(b []byte) (serializeable, error) {
		v, err := ParseNotify(b)
		if err != nil {
			return nil, err
		}
		v.Payload = nil
		return v, nil
	})
}
